package IncrementDecremnet;

public class IncrementDecremnet {
    public static void main(String[] args) {
        int x = 10,a=10, y,z;
        z = a++; // a = 11 , z = 10
        y = ++x; // x = 11 , y = 11

        System.out.println("a = "+a+" z = "+z);
        System.out.println("x = "+a+" y = "+y);

    }
}