import java.lang.*;

public class Variables 
{
	public static void main(String arg[])
	{
		byte a = 100;
		short b = 300;
		int x = 1000;
		
		float f = 96.6f;
		char c = 'A';
		
		System.out.println(a);
		System.out.println(b);
		System.out.println(x);
		System.out.println(f);
		System.out.println(c);
		
	}
}